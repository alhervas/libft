#include<stdio.h>

int	ft_isalpha(int a)
{
	if ((a >= 'a' && a <= 'z') ||
			(a >= 'A' && a <= 'Z'))
		return(1);
	return (0);
}
int main()
{
	printf("%d", ft_isalpha('7'));
}
