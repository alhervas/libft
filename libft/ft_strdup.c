#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*s1;
	char	*s2;

	s1 = (char *)s;
	s2 = (char *)malloc(ft_strlen(s1) + 1);
	if (!(s2))
		return (0);
	ft_memcpy(s2, s1, ft_strlen(s1) + 1);
	return (s2);
}
